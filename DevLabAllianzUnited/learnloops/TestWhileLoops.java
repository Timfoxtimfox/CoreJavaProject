package com.test.learnloops;

/*Print Table of 5 and 10 using loops */
public class TestWhileLoops {
	
	public static void main(String[] args) {
		
	        int j=1;
	        while (j<=10)
	        {
	            int five=5*j;
	            System.out.println(j+"X5="+five);
	            j++;
	        }
	        
	    // 5*1 = 5 
	    // 5*2 = 10
	    // 5*3 = 10
	    // 5*4 = 20
	        
		
		
	}
	

}
